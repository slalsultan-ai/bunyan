export async function seedWeeklyContent(): Promise<{ inserted: number; skipped: number }> {
  const db = getDb();
  let inserted = 0;
  let skipped = 0;

  for (const entry of CONTENT) {
    // Check if already exists
    const existing = await db
      .select()
      .from(weeklyEmailContent)
      .where(and(
        eq(weeklyEmailContent.weekNumber, entry.weekNumber),
        eq(weeklyEmailContent.ageGroup, entry.ageGroup),
      ))
      .limit(1);

    if (existing.length > 0) {
      skipped++;
      continue;
    }

    await db.insert(weeklyEmailContent).values({
      id: crypto.randomUUID(),
      weekNumber: entry.weekNumber,
      ageGroup: entry.ageGroup,
      content: entry as never,
    });
    inserted++;
  }

  return { inserted, skipped };
}

export async function getWeeklyContent(weekNumber: number, ageGroup: string): Promise<WeeklyContent | null> {
  const db = getDb();
  const [row] = await db
    .select()
    .from(weeklyEmailContent)
    .where(and(
      eq(weeklyEmailContent.weekNumber, weekNumber),
      eq(weeklyEmailContent.ageGroup, ageGroup),
    ))
    .limit(1);

  if (!row) return null;
  return row.content as WeeklyContent;
}
